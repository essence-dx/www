#[derive(Default)]
struct Dict {
    ids: BTreeMap<String, u32>,
    values: Vec<String>,
}

impl Dict {
    fn intern(&mut self, value: &str) -> u32 {
        if value.is_empty() {
            return 0;
        }
        if let Some(id) = self.ids.get(value) {
            return *id;
        }
        let id = (self.values.len() + 1) as u32;
        self.ids.insert(value.to_owned(), id);
        self.values.push(value.to_owned());
        id
    }

    fn write(&self, out: &mut Vec<u8>) {
        put_varint(out, self.values.len() as u64);
        for value in &self.values {
            put_bytes(out, value.as_bytes());
        }
    }
}

fn main() -> Result<(), Box<dyn std::error::Error>> {
    let scenarios = scenarios();
    let mut results = Vec::new();

    for scenario in &scenarios {
        let html = render_html(&scenario.page);
        let json = serde_json::to_vec(&scenario.page)?;
        let rkyv = rkyv::to_bytes::<rkyv::rancor::Error>(&scenario.page)?.to_vec();
        let dict = encode_dict_packet(&scenario.page);
        let template = scenario.template_packet.clone();

        let json_for_access = json.clone();
        results.push(measure_encoding(
            scenario.name,
            "html-string",
            &html,
            || black_box(scan_html(&html)),
        ));
        results.push(measure_encoding(
            scenario.name,
            "json-serde",
            &json,
            || {
                let decoded: Page = serde_json::from_slice(&json_for_access).unwrap();
                black_box(decoded.nodes.len() as u64)
            },
        ));

        let rkyv_for_access = rkyv.clone();
        results.push(measure_encoding(
            scenario.name,
            "rkyv-zero-copy",
            &rkyv,
            || unsafe {
                let archived = rkyv::access_unchecked::<ArchivedPage>(&rkyv_for_access);
                black_box(archived.nodes.len() as u64)
            },
        ));

        let dict_for_access = dict.clone();
        results.push(measure_encoding(
            scenario.name,
            "dx-dict-packet",
            &dict,
            || black_box(scan_dict_packet(&dict_for_access)),
        ));

        let template_for_access = template.clone();
        results.push(measure_encoding(
            scenario.name,
            "dx-template-data",
            &template,
            || black_box(scan_template_packet(&template_for_access)),
        ));
    }

    let largest = scenarios
        .iter()
        .max_by_key(|scenario| scenario.template_packet.len())
        .expect("scenarios exist");
    let mmap = measure_mmap(largest.name, &largest.template_packet)?;
    let breakthroughs = breakthrough_experiments()?;

    let report = Report {
        generated_at_note: "Generated by local binary-web-lab on current machine".to_owned(),
        results,
        mmap,
        breakthroughs,
        findings: vec![
            "Template/data separation is the strongest size lever for repeated UI.".to_owned(),
            "Columnar slot data and semantic codecs are the next jump after template/data packets.".to_owned(),
            "Patch streams are the biggest live-app advantage: changed cells can be shipped as operations instead of HTML or JSON trees.".to_owned(),
            "rkyv is excellent for server/build zero-copy access, but generic archived object graphs are not automatically the smallest browser payload.".to_owned(),
            "memmap2 helps build/server/edge cache startup for large packets; browsers still need fetched ArrayBuffers or streams.".to_owned(),
            "Tiny pages need static or micro-JS mode; WASM should only ship when its fixed cost is amortized.".to_owned(),
        ],
    };

    write_report(&report)?;
    print_summary(&report);
    Ok(())
}

