fn put_slots(out: &mut Vec<u8>, slots: &[&str]) {
    put_varint(out, slots.len() as u64);
    for slot in slots {
        put_bytes(out, slot.as_bytes());
    }
}

fn put_events(out: &mut Vec<u8>, events: &[&str]) {
    put_varint(out, events.len() as u64);
    for event in events {
        put_bytes(out, event.as_bytes());
    }
}

fn put_bytes(out: &mut Vec<u8>, bytes: &[u8]) {
    put_varint(out, bytes.len() as u64);
    out.extend_from_slice(bytes);
}

fn put_varint(out: &mut Vec<u8>, mut value: u64) {
    while value >= 0x80 {
        out.push((value as u8) | 0x80);
        value >>= 7;
    }
    out.push(value as u8);
}

fn read_varint(bytes: &[u8], offset: &mut usize) -> u64 {
    let mut value = 0u64;
    let mut shift = 0;
    loop {
        let byte = bytes[*offset];
        *offset += 1;
        value |= ((byte & 0x7f) as u64) << shift;
        if byte & 0x80 == 0 {
            break;
        }
        shift += 7;
    }
    value
}

fn skip_bytes(bytes: &[u8], offset: &mut usize) -> usize {
    let len = read_varint(bytes, offset) as usize;
    *offset += len;
    len
}

fn scan_html(bytes: &[u8]) -> u64 {
    bytes.iter().filter(|byte| **byte == b'<').count() as u64
}

fn scan_dict_packet(bytes: &[u8]) -> u64 {
    let mut offset = 4;
    let string_count = read_varint(bytes, &mut offset);
    let mut sum = string_count;
    for _ in 0..string_count {
        sum += skip_bytes(bytes, &mut offset) as u64;
    }
    let node_count = read_varint(bytes, &mut offset);
    sum += node_count;
    for _ in 0..node_count {
        for _ in 0..4 {
            sum += read_varint(bytes, &mut offset);
        }
        let attrs = read_varint(bytes, &mut offset);
        for _ in 0..attrs {
            sum += read_varint(bytes, &mut offset);
            sum += read_varint(bytes, &mut offset);
        }
        let events = read_varint(bytes, &mut offset);
        for _ in 0..events {
            sum += read_varint(bytes, &mut offset);
            sum += read_varint(bytes, &mut offset);
            sum += read_varint(bytes, &mut offset);
        }
    }
    sum
}

fn scan_template_packet(bytes: &[u8]) -> u64 {
    let mut offset = 4;
    let mut sum = skip_bytes(bytes, &mut offset) as u64;
    let instances = read_varint(bytes, &mut offset);
    sum += instances;
    for _ in 0..instances {
        let slots = read_varint(bytes, &mut offset);
        sum += slots;
        for _ in 0..slots {
            sum += skip_bytes(bytes, &mut offset) as u64;
        }
    }
    while offset < bytes.len() {
        sum += skip_bytes(bytes, &mut offset) as u64;
    }
    sum
}

fn scan_columnar_table(bytes: &[u8]) -> u64 {
    let mut offset = 4;
    let rows = read_varint(bytes, &mut offset) as usize;
    let mut sum = rows as u64;
    for _ in 0..rows {
        sum += skip_bytes(bytes, &mut offset) as u64;
    }
    offset += rows * 2;
    for _ in 0..rows {
        sum += read_varint(bytes, &mut offset);
    }
    sum + offset as u64
}

fn scan_semantic_table(bytes: &[u8]) -> u64 {
    let mut offset = 4;
    let rows = read_varint(bytes, &mut offset);
    let prefix_len = skip_bytes(bytes, &mut offset) as u64;
    while offset < bytes.len() {
        read_varint(bytes, &mut offset);
    }
    rows + prefix_len + offset as u64
}

fn scan_columnar_viewport(bytes: &[u8]) -> u64 {
    let mut offset = 4;
    let total_rows = read_varint(bytes, &mut offset);
    let visible_rows = read_varint(bytes, &mut offset);
    let mut sum = total_rows + visible_rows;
    for _ in 0..visible_rows {
        sum += skip_bytes(bytes, &mut offset) as u64;
        offset += 2;
        sum += read_varint(bytes, &mut offset);
    }
    sum
}

fn scan_dx_cell_patch(bytes: &[u8]) -> u64 {
    let mut offset = 4;
    let changes = read_varint(bytes, &mut offset);
    let mut sum = changes;
    for _ in 0..(changes / 2) {
        sum += read_varint(bytes, &mut offset);
        offset += 2;
        sum += read_varint(bytes, &mut offset);
        sum += read_varint(bytes, &mut offset);
        offset += 2;
        sum += read_varint(bytes, &mut offset);
    }
    sum
}

fn scan_dx_range_patch(bytes: &[u8]) -> u64 {
    let mut offset = 4;
    let start = read_varint(bytes, &mut offset);
    let end = read_varint(bytes, &mut offset);
    offset += 2;
    start + end + offset as u64
}

fn measure_encoding<F>(scenario: &str, encoding: &str, bytes: &[u8], mut access: F) -> EncodingResult
where
    F: FnMut() -> u64,
{
    const OPS_PER_SAMPLE: u32 = 20;
    let mut samples = Vec::with_capacity(140);
    for _ in 0..20 {
        black_box(access());
    }
    for _ in 0..140 {
        let start = Instant::now();
        let mut sum = 0u64;
        for _ in 0..OPS_PER_SAMPLE {
            sum = sum.wrapping_add(access());
        }
        black_box(sum);
        samples.push(start.elapsed() / OPS_PER_SAMPLE);
    }
    let (median, p95) = median_p95(&mut samples);
    EncodingResult {
        scenario: scenario.to_owned(),
        encoding: encoding.to_owned(),
        raw_bytes: bytes.len(),
        gzip_bytes: gzip_len(bytes),
        brotli_bytes: brotli_len(bytes),
        access_median_ns: median.as_nanos(),
        access_p95_ns: p95.as_nanos(),
    }
}

fn breakthrough_experiments() -> Result<Vec<BreakthroughResult>, Box<dyn std::error::Error>> {
    let rows = 1200;
    let changes = changed_indices(rows, 12);
    let mut results = Vec::new();

    let template = encode_table_template(rows);
    let columnar = encode_columnar_table(rows);
    let semantic = encode_semantic_table(rows);
    let viewport = encode_columnar_viewport(rows, 40);

    let template_for_access = template.clone();
    results.push(measure_breakthrough(
        "dashboard-full-data",
        "dx-template-slots",
        &template,
        || black_box(scan_template_packet(&template_for_access)),
    ));

    let columnar_for_access = columnar.clone();
    results.push(measure_breakthrough(
        "dashboard-full-data",
        "dx-columnar-slots",
        &columnar,
        || black_box(scan_columnar_table(&columnar_for_access)),
    ));

    let semantic_for_access = semantic.clone();
    results.push(measure_breakthrough(
        "dashboard-full-data",
        "dx-semantic-codec",
        &semantic,
        || black_box(scan_semantic_table(&semantic_for_access)),
    ));

    let viewport_for_access = viewport.clone();
    results.push(measure_breakthrough(
        "initial-viewport",
        "dx-viewport-40",
        &viewport,
        || black_box(scan_columnar_viewport(&viewport_for_access)),
    ));

    let html_patch = encode_html_cell_updates(&changes);
    let json_patch = encode_json_cell_patch(&changes)?;
    let dx_patch = encode_dx_cell_patch(&changes);

    results.push(measure_breakthrough(
        "12-row-live-update",
        "html-row-fragments",
        &html_patch,
        || black_box(scan_html(&html_patch)),
    ));

    let json_patch_for_access = json_patch.clone();
    results.push(measure_breakthrough(
        "12-row-live-update",
        "json-cell-patch",
        &json_patch,
        || {
            let decoded: Vec<JsonPatchCellOwned> =
                serde_json::from_slice(&json_patch_for_access).unwrap();
            black_box(
                decoded
                    .iter()
                    .map(|item| item.row as u64 + item.column.len() as u64 + item.value.len() as u64)
                    .sum::<u64>(),
            )
        },
    ));

    let dx_patch_for_access = dx_patch.clone();
    results.push(measure_breakthrough(
        "12-row-live-update",
        "dx-cell-patch",
        &dx_patch,
        || black_box(scan_dx_cell_patch(&dx_patch_for_access)),
    ));

    let html_bulk = encode_html_bulk_rows(300, 900);
    let json_range = encode_json_range_patch(300, 900)?;
    let dx_range = encode_dx_range_patch(300, 900);

    results.push(measure_breakthrough(
        "600-row-bulk-update",
        "html-row-fragments",
        &html_bulk,
        || black_box(scan_html(&html_bulk)),
    ));

    let json_range_for_access = json_range.clone();
    results.push(measure_breakthrough(
        "600-row-bulk-update",
        "json-range-op",
        &json_range,
        || {
            let decoded: JsonRangePatchOwned =
                serde_json::from_slice(&json_range_for_access).unwrap();
            black_box(
                (decoded.end - decoded.start) as u64
                    + decoded.op.len() as u64
                    + decoded.column.len() as u64
                    + decoded.value.len() as u64,
            )
        },
    ));

    let dx_range_for_access = dx_range.clone();
    results.push(measure_breakthrough(
        "600-row-bulk-update",
        "dx-range-op",
        &dx_range,
        || black_box(scan_dx_range_patch(&dx_range_for_access)),
    ));

    Ok(results)
}

#[derive(Deserialize)]
struct JsonPatchCellOwned {
    row: usize,
    column: String,
    value: String,
}

#[derive(Deserialize)]
struct JsonRangePatchOwned {
    op: String,
    start: usize,
    end: usize,
    column: String,
    value: String,
}

fn measure_breakthrough<F>(
    experiment: &str,
    strategy: &str,
    bytes: &[u8],
    mut access: F,
) -> BreakthroughResult
where
    F: FnMut() -> u64,
{
    const OPS_PER_SAMPLE: u32 = 40;
    let mut samples = Vec::with_capacity(160);
    for _ in 0..20 {
        black_box(access());
    }
    for _ in 0..160 {
        let start = Instant::now();
        let mut sum = 0u64;
        for _ in 0..OPS_PER_SAMPLE {
            sum = sum.wrapping_add(access());
        }
        black_box(sum);
        samples.push(start.elapsed() / OPS_PER_SAMPLE);
    }
    let (median, p95) = median_p95(&mut samples);
    BreakthroughResult {
        experiment: experiment.to_owned(),
        strategy: strategy.to_owned(),
        raw_bytes: bytes.len(),
        gzip_bytes: gzip_len(bytes),
        brotli_bytes: brotli_len(bytes),
        access_median_ns: median.as_nanos(),
        access_p95_ns: p95.as_nanos(),
    }
}

fn measure_mmap(scenario: &str, bytes: &[u8]) -> Result<MmapResult, Box<dyn std::error::Error>> {
    let dir = tempfile::tempdir()?;
    let path = dir.path().join("packet.dxp");
    fs::write(&path, bytes)?;
    let mut read_samples = Vec::new();
    let mut mmap_samples = Vec::new();

    for _ in 0..80 {
        let start = Instant::now();
        let data = fs::read(&path)?;
        black_box(scan_template_packet(&data));
        read_samples.push(start.elapsed());
    }

    for _ in 0..80 {
        let start = Instant::now();
        let file = File::open(&path)?;
        let mmap = unsafe { Mmap::map(&file)? };
        black_box(scan_template_packet(&mmap));
        mmap_samples.push(start.elapsed());
    }

    let (read_median, read_p95) = median_p95(&mut read_samples);
    let (mmap_median, mmap_p95) = median_p95(&mut mmap_samples);

    Ok(MmapResult {
        scenario: scenario.to_owned(),
        read_to_vec_median_ns: read_median.as_nanos(),
        mmap_median_ns: mmap_median.as_nanos(),
        read_to_vec_p95_ns: read_p95.as_nanos(),
        mmap_p95_ns: mmap_p95.as_nanos(),
        bytes: bytes.len(),
    })
}

fn gzip_len(bytes: &[u8]) -> usize {
    let mut encoder = GzEncoder::new(Vec::new(), Compression::best());
    encoder.write_all(bytes).unwrap();
    encoder.finish().unwrap().len()
}

fn brotli_len(bytes: &[u8]) -> usize {
    let mut out = Vec::new();
    {
        let mut compressor = CompressorWriter::new(&mut out, 4096, 11, 22);
        compressor.write_all(bytes).unwrap();
    }
    out.len()
}

fn median_p95(samples: &mut [Duration]) -> (Duration, Duration) {
    samples.sort();
    let median = samples[samples.len() / 2];
    let p95 = samples[((samples.len() as f64 * 0.95).ceil() as usize).min(samples.len() - 1)];
    (median, p95)
}

fn write_report(report: &Report) -> Result<(), Box<dyn std::error::Error>> {
    let manifest_dir = PathBuf::from(env!("CARGO_MANIFEST_DIR"));
    let benchmarks_dir = manifest_dir.parent().expect("lab is under benchmarks");
    let report_dir = benchmarks_dir.join("reports");
    fs::create_dir_all(&report_dir)?;
    let json = serde_json::to_string_pretty(report)?;
    fs::write(report_dir.join("binary-web-lab.json"), format!("{json}\n"))?;
    fs::write(report_dir.join("binary-web-lab.md"), markdown(report))?;
    Ok(())
}

fn markdown(report: &Report) -> String {
    let mut out = String::new();
    out.push_str("# Binary Web Lab\n\n");
    out.push_str("Local experiment comparing HTML, JSON, rkyv, dictionary packets, and template/data packets for DX-WWW-like page graphs.\n\n");
    out.push_str("| Scenario | Encoding | Raw | gzip | Brotli | Access median |\n");
    out.push_str("| --- | --- | ---: | ---: | ---: | ---: |\n");
    for result in &report.results {
        out.push_str(&format!(
            "| {} | {} | {} | {} | {} | {} ns |\n",
            result.scenario,
            result.encoding,
            result.raw_bytes,
            result.gzip_bytes,
            result.brotli_bytes,
            result.access_median_ns
        ));
    }
    out.push_str("\n## Mmap\n\n");
    out.push_str(&format!(
        "- Scenario: {}\n- Packet bytes: {}\n- read_to_vec median: {} ns\n- mmap median: {} ns\n",
        report.mmap.scenario,
        report.mmap.bytes,
        report.mmap.read_to_vec_median_ns,
        report.mmap.mmap_median_ns
    ));
    out.push_str("\n## Breakthrough Experiments\n\n");
    out.push_str("| Experiment | Strategy | Raw | gzip | Brotli | Access median |\n");
    out.push_str("| --- | --- | ---: | ---: | ---: | ---: |\n");
    for result in &report.breakthroughs {
        out.push_str(&format!(
            "| {} | {} | {} | {} | {} | {} ns |\n",
            result.experiment,
            result.strategy,
            result.raw_bytes,
            result.gzip_bytes,
            result.brotli_bytes,
            result.access_median_ns
        ));
    }
    out.push_str("\n## Findings\n\n");
    for finding in &report.findings {
        out.push_str("- ");
        out.push_str(finding);
        out.push('\n');
    }
    out
}

