fn base_page(name: &str) -> Page {
    Page {
        route: format!("/{name}"),
        title: format!("{name} benchmark"),
        nodes: Vec::new(),
    }
}

fn push_node(page: &mut Page, parent: u32, tag: &str, class_name: &str, text: &str) -> u32 {
    let id = page.nodes.len() as u32;
    page.nodes.push(Node {
        parent,
        tag: tag.to_owned(),
        class_name: class_name.to_owned(),
        text: text.to_owned(),
        attrs: Vec::new(),
        events: Vec::new(),
    });
    id
}

