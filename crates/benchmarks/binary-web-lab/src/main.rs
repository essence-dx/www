fn render_html(page: &Page) -> Vec<u8> {
    let mut out = String::new();
    out.push_str("<!doctype html><html><head><meta charset=\"utf-8\"><title>");
    out.push_str(&page.title);
    out.push_str("</title></head><body>");
    for node in &page.nodes {
        out.push('<');
        out.push_str(&node.tag);
        if !node.class_name.is_empty() {
            out.push_str(" class=\"");
            out.push_str(&node.class_name);
            out.push('"');
        }
        for (key, value) in &node.attrs {
            out.push(' ');
            out.push_str(key);
            out.push_str("=\"");
            out.push_str(value);
            out.push('"');
        }
        out.push('>');
        out.push_str(&node.text);
        out.push_str("</");
        out.push_str(&node.tag);
        out.push('>');
    }
    out.push_str("</body></html>");
    out.into_bytes()
}

fn encode_dict_packet(page: &Page) -> Vec<u8> {
    let mut dict = Dict::default();
    dict.intern(&page.route);
    dict.intern(&page.title);
    for node in &page.nodes {
        dict.intern(&node.tag);
        dict.intern(&node.class_name);
        dict.intern(&node.text);
        for (key, value) in &node.attrs {
            dict.intern(key);
            dict.intern(value);
        }
        for event in &node.events {
            dict.intern(&event.trigger);
            dict.intern(&event.action);
            dict.intern(&event.target);
        }
    }

    let mut out = Vec::new();
    out.extend_from_slice(b"DXD1");
    dict.write(&mut out);
    put_varint(&mut out, page.nodes.len() as u64);
    for node in &page.nodes {
        put_varint(&mut out, node.parent as u64);
        put_varint(&mut out, dict.intern(&node.tag) as u64);
        put_varint(&mut out, dict.intern(&node.class_name) as u64);
        put_varint(&mut out, dict.intern(&node.text) as u64);
        put_varint(&mut out, node.attrs.len() as u64);
        for (key, value) in &node.attrs {
            put_varint(&mut out, dict.intern(key) as u64);
            put_varint(&mut out, dict.intern(value) as u64);
        }
        put_varint(&mut out, node.events.len() as u64);
        for event in &node.events {
            put_varint(&mut out, dict.intern(&event.trigger) as u64);
            put_varint(&mut out, dict.intern(&event.action) as u64);
            put_varint(&mut out, dict.intern(&event.target) as u64);
        }
    }
    out
}

