#[cfg(test)]
mod tests {
    use super::*;
    use crate::parser::style::StyleParser;

    #[test]
    fn test_compile_empty_style() {
        let compiler = StyleCompiler::new();
        let style = ParsedStyle {
            source: String::new(),
            scoped: false,
            rules: vec![],
            atomic_classes: vec![],
            custom_properties: vec![],
            imports: vec![],
        };

        let output = compiler.compile(&style).unwrap();

        // Check header
        assert_eq!(&output[0..4], b"DXC1");
        assert_eq!(output[4], 1); // Version
    }

    #[test]
    fn test_compile_simple_rule() {
        let compiler = StyleCompiler::new();
        let style = ParsedStyle {
            source: ".test { color: red; }".to_string(),
            scoped: false,
            rules: vec![CssRule {
                selector: ".test".to_string(),
                declarations: vec![CssDeclaration {
                    property: "color".to_string(),
                    value: "red".to_string(),
                    important: false,
                }],
                at_rule: None,
            }],
            atomic_classes: vec![],
            custom_properties: vec![],
            imports: vec![],
        };

        let output = compiler.compile(&style).unwrap();

        // Check header
        assert_eq!(&output[0..4], b"DXC1");
        // Should have rule type normal
        assert!(output.iter().any(|&b| b == RuleType::Normal as u8));
    }

    #[test]
    fn test_classify_selector() {
        assert_eq!(classify_selector("*"), SelectorType::Universal);
        assert_eq!(classify_selector("#id"), SelectorType::Id);
        assert_eq!(classify_selector(".class"), SelectorType::Class);
        assert_eq!(classify_selector("div"), SelectorType::Tag);
        assert_eq!(classify_selector("div .class"), SelectorType::Compound);
    }

    #[test]
    fn compile_normal_css_renders_nested_container_rule() {
        let style = StyleParser::new()
            .parse(
                "@container card (min-width: 32rem) { .container-card { display: grid; } }",
                false,
                std::path::Path::new("styles/app.css"),
            )
            .expect("parse css");
        let output = StyleCompiler::new()
            .compile_normal_css(&style)
            .expect("normal css");

        assert!(output.contains("@container card (min-width: 32rem) {\n"));
        assert!(output.contains("  .container-card {\n"));
        assert!(output.contains("    display: grid;\n"));
    }

    #[test]
    fn compile_normal_css_renders_stacked_conditional_rules() {
        let style = StyleParser::new()
            .parse(
                "@supports (display: grid) { @media (min-width: 40rem) { .stacked-card { display: grid; } } }",
                false,
                std::path::Path::new("styles/app.css"),
            )
            .expect("parse css");
        let output = StyleCompiler::new()
            .compile_normal_css(&style)
            .expect("normal css");

        assert!(output.contains("@supports (display: grid) {\n"));
        assert!(output.contains("  @media (min-width: 40rem) {\n"));
        assert!(output.contains("    .stacked-card {\n"));
        assert!(output.contains("      display: grid;\n"));
    }
}
