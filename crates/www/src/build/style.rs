//! # Style Compiler
//!
//! Compiles parsed CSS into the public normal CSS output path.
//!
//! DXC1 remains available only for internal compatibility and is not part of
//! the DX-WWW public dx-style launch pitch.
//!
//! ## Binary Format (DXC1)
//!
//! ```text
//! Header (8 bytes):
//!   [4] Magic: "DXC1"
//!   [1] Version
//!   [1] Flags (scoped, has variables, has animations)
//!   [2] Reserved
//!
//! String Pool:
//!   [4] Count
//!   For each string:
//!     [2] Length
//!     [N] UTF-8 bytes
//!
//! Rules:
//!   [4] Rule count
//!   For each rule:
//!     [1] Rule type
//!     ... type-specific data
//! ```

use crate::error::DxResult;
use crate::parser::style::{AtRule, CssDeclaration, CssRule, ParsedStyle};
use std::collections::HashMap;

/// Magic bytes for DXC1 format.
pub const STYLE_MAGIC: &[u8; 4] = b"DXC1";

/// Current version of the style format.
pub const STYLE_VERSION: u8 = 1;

/// Public launch output mode for DX-WWW style builds.
pub const STYLE_PUBLIC_OUTPUT_MODE: &str = "normal-generated-css";

/// Binary style output status for launch documentation and guards.
pub const STYLE_BINARY_OUTPUT_STATUS: &str = "deprecated-internal";

/// Rule type opcodes.
#[repr(u8)]
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum RuleType {
    Normal = 0x01,
    Media = 0x02,
    Keyframes = 0x03,
    Supports = 0x04,
    Layer = 0x05,
    Container = 0x06,
    Nested = 0x07,
}

/// Selector type for optimization.
#[repr(u8)]
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum SelectorType {
    Class = 0x01,
    Id = 0x02,
    Tag = 0x03,
    Universal = 0x04,
    Compound = 0x05,
}

/// Compiles styles for the public normal CSS path and internal binary path.
#[derive(Debug, Default)]
pub struct StyleCompiler;

impl StyleCompiler {
    /// Create a new style compiler.
    pub fn new() -> Self {
        Self
    }

    /// Compile a parsed style to normal CSS for the public launch path.
    pub fn compile_normal_css(&self, style: &ParsedStyle) -> DxResult<String> {
        let mut output = String::new();
        output.push_str("/* Generated by dx style build. */\n");
        output.push_str(&format!(
            "/* dx-style output: {STYLE_PUBLIC_OUTPUT_MODE} */\n"
        ));
        output.push_str(&format!(
            "/* binary style output: {STYLE_BINARY_OUTPUT_STATUS} */\n\n"
        ));

        for import in &style.imports {
            output.push_str("@import \"");
            output.push_str(&import.url);
            output.push('"');
            if let Some(media) = &import.media {
                output.push(' ');
                output.push_str(media);
            }
            output.push_str(";\n");
        }
        if !style.imports.is_empty() {
            output.push('\n');
        }

        for rule in &style.rules {
            render_css_rule(&mut output, rule);
        }

        Ok(output)
    }

    /// Compile a parsed style to the internal DXC1 binary format.
    pub fn compile(&self, style: &ParsedStyle) -> DxResult<Vec<u8>> {
        let mut output = Vec::with_capacity(4096);

        // Write header
        output.extend_from_slice(STYLE_MAGIC);
        output.push(STYLE_VERSION);

        // Flags
        let mut flags: u8 = 0;
        if style.scoped {
            flags |= 0x01;
        }
        if !style.custom_properties.is_empty() {
            flags |= 0x02;
        }
        if style
            .rules
            .iter()
            .any(|r| r.at_rule.as_ref().is_some_and(at_rule_contains_keyframes))
        {
            flags |= 0x04;
        }
        output.push(flags);

        // Reserved bytes
        output.extend_from_slice(&[0u8; 2]);

        // Build string pool
        let mut compiler = StyleCompilerInner::new();
        for rule in &style.rules {
            compiler.collect_strings(rule);
        }

        // Write string pool
        compiler.write_string_pool(&mut output)?;

        // Write rule count
        output.extend_from_slice(&(style.rules.len() as u32).to_le_bytes());

        // Compile rules
        for rule in &style.rules {
            compiler.compile_rule(&mut output, rule)?;
        }

        Ok(output)
    }
}

fn render_css_rule(output: &mut String, rule: &CssRule) {
    match &rule.at_rule {
        Some(AtRule::Keyframes { name }) => {
            output.push_str("@keyframes ");
            output.push_str(name);
            output.push_str(" {\n");
            render_css_declarations(output, &rule.declarations);
            output.push_str("}\n\n");
        }
        Some(
            at_rule @ (AtRule::Media { .. }
            | AtRule::Container { .. }
            | AtRule::Supports { .. }
            | AtRule::Layer { .. }),
        ) => render_normal_css_at_rule_stack(output, std::slice::from_ref(at_rule), rule),
        Some(AtRule::Nested { rules }) => render_normal_css_at_rule_stack(output, rules, rule),
        Some(AtRule::Import { .. }) => {}
        None => {
            render_normal_css_nested_rule(output, rule, 0);
            output.push('\n');
        }
    }
}

fn render_css_declarations(output: &mut String, declarations: &[CssDeclaration]) {
    render_css_declarations_with_indent(output, declarations, 1);
}

fn render_css_declarations_with_indent(
    output: &mut String,
    declarations: &[CssDeclaration],
    indent_level: usize,
) {
    for declaration in declarations {
        push_css_indent(output, indent_level);
        output.push_str(&declaration.property);
        output.push_str(": ");
        output.push_str(&declaration.value);
        if declaration.important {
            output.push_str(" !important");
        }
        output.push_str(";\n");
    }
}

fn render_normal_css_at_rule_stack(output: &mut String, rules: &[AtRule], rule: &CssRule) {
    let mut close_count = 0usize;
    for at_rule in rules {
        close_count += render_normal_css_at_rule_start(output, at_rule, close_count);
    }

    render_normal_css_nested_rule(output, rule, close_count);
    while close_count > 0 {
        close_count -= 1;
        push_css_indent(output, close_count);
        output.push_str("}\n");
    }
    output.push('\n');
}

fn render_normal_css_at_rule_start(
    output: &mut String,
    at_rule: &AtRule,
    indent_level: usize,
) -> usize {
    match at_rule {
        AtRule::Media { query } => {
            push_css_indent(output, indent_level);
            output.push_str("@media ");
            output.push_str(query);
            output.push_str(" {\n");
            1
        }
        AtRule::Container { query } => {
            push_css_indent(output, indent_level);
            output.push_str("@container ");
            output.push_str(query);
            output.push_str(" {\n");
            1
        }
        AtRule::Supports { condition } => {
            push_css_indent(output, indent_level);
            output.push_str("@supports ");
            output.push_str(condition);
            output.push_str(" {\n");
            1
        }
        AtRule::Layer { name } => {
            push_css_indent(output, indent_level);
            output.push_str("@layer");
            if let Some(name) = name {
                output.push(' ');
                output.push_str(name);
            }
            output.push_str(" {\n");
            1
        }
        AtRule::Nested { rules } => {
            let mut opened = 0usize;
            for rule in rules {
                opened += render_normal_css_at_rule_start(output, rule, indent_level + opened);
            }
            opened
        }
        AtRule::Keyframes { name } => {
            push_css_indent(output, indent_level);
            output.push_str("@keyframes ");
            output.push_str(name);
            output.push_str(" {\n");
            1
        }
        AtRule::Import { .. } => 0,
    }
}

fn render_normal_css_nested_rule(output: &mut String, rule: &CssRule, indent_level: usize) {
    push_css_indent(output, indent_level);
    output.push_str(&rule.selector);
    output.push_str(" {\n");
    render_css_declarations_with_indent(output, &rule.declarations, indent_level + 1);
    push_css_indent(output, indent_level);
    output.push_str("}\n");
}

fn push_css_indent(output: &mut String, indent_level: usize) {
    for _ in 0..indent_level {
        output.push_str("  ");
    }
}

struct StyleCompilerInner {
    string_pool: Vec<String>,
    string_map: HashMap<String, u32>,
}

impl StyleCompilerInner {
    fn new() -> Self {
        Self {
            string_pool: Vec::new(),
            string_map: HashMap::new(),
        }
    }

    fn intern_string(&mut self, s: &str) -> u32 {
        if let Some(&idx) = self.string_map.get(s) {
            return idx;
        }
        let idx = self.string_pool.len() as u32;
        self.string_pool.push(s.to_string());
        self.string_map.insert(s.to_string(), idx);
        idx
    }

    fn collect_strings(&mut self, rule: &CssRule) {
        self.intern_string(&rule.selector);

        for decl in &rule.declarations {
            self.intern_string(&decl.property);
            self.intern_string(&decl.value);
        }

        if let Some(ref at_rule) = rule.at_rule {
            self.collect_at_rule_strings(at_rule);
        }
    }

    fn collect_at_rule_strings(&mut self, at_rule: &AtRule) {
        match at_rule {
            AtRule::Media { query } | AtRule::Container { query } => {
                self.intern_string(query);
            }
            AtRule::Keyframes { name } => {
                self.intern_string(name);
            }
            AtRule::Import { url } => {
                self.intern_string(url);
            }
            AtRule::Supports { condition } => {
                self.intern_string(condition);
            }
            AtRule::Layer { name } => {
                if let Some(n) = name {
                    self.intern_string(n);
                }
            }
            AtRule::Nested { rules } => {
                for rule in rules {
                    self.collect_at_rule_strings(rule);
                }
            }
        }
    }

    fn write_string_pool(&self, output: &mut Vec<u8>) -> DxResult<()> {
        output.extend_from_slice(&(self.string_pool.len() as u32).to_le_bytes());

        for s in &self.string_pool {
            let bytes = s.as_bytes();
            output.extend_from_slice(&(bytes.len() as u16).to_le_bytes());
            output.extend_from_slice(bytes);
        }

        Ok(())
    }

    fn compile_rule(&mut self, output: &mut Vec<u8>, rule: &CssRule) -> DxResult<()> {
        // Rule type
        let rule_type = rule
            .at_rule
            .as_ref()
            .map(rule_type_for_at_rule)
            .unwrap_or(RuleType::Normal);
        output.push(rule_type as u8);

        // At-rule specific data
        if let Some(ref at_rule) = rule.at_rule {
            self.compile_at_rule_payload(output, at_rule);
        }

        // Selector
        let selector_idx = self.intern_string(&rule.selector);
        output.extend_from_slice(&selector_idx.to_le_bytes());

        // Selector type hint
        let selector_type = classify_selector(&rule.selector);
        output.push(selector_type as u8);

        // Declaration count
        output.push(rule.declarations.len() as u8);

        // Declarations
        for decl in &rule.declarations {
            self.compile_declaration(output, decl)?;
        }

        Ok(())
    }

    fn compile_at_rule_payload(&mut self, output: &mut Vec<u8>, at_rule: &AtRule) {
        match at_rule {
            AtRule::Media { query } | AtRule::Container { query } => {
                let query_idx = self.intern_string(query);
                output.extend_from_slice(&query_idx.to_le_bytes());
            }
            AtRule::Keyframes { name } => {
                let name_idx = self.intern_string(name);
                output.extend_from_slice(&name_idx.to_le_bytes());
            }
            AtRule::Supports { condition } => {
                let cond_idx = self.intern_string(condition);
                output.extend_from_slice(&cond_idx.to_le_bytes());
            }
            AtRule::Layer { name } => {
                if let Some(n) = name {
                    output.push(0x01);
                    let name_idx = self.intern_string(n);
                    output.extend_from_slice(&name_idx.to_le_bytes());
                } else {
                    output.push(0x00);
                }
            }
            AtRule::Import { url } => {
                let url_idx = self.intern_string(url);
                output.extend_from_slice(&url_idx.to_le_bytes());
            }
            AtRule::Nested { rules } => {
                output.push(rules.len() as u8);
                for rule in rules {
                    output.push(rule_type_for_at_rule(rule) as u8);
                    self.compile_at_rule_payload(output, rule);
                }
            }
        }
    }

    fn compile_declaration(&mut self, output: &mut Vec<u8>, decl: &CssDeclaration) -> DxResult<()> {
        // Property
        let prop_idx = self.intern_string(&decl.property);
        output.extend_from_slice(&prop_idx.to_le_bytes());

        // Value
        let val_idx = self.intern_string(&decl.value);
        output.extend_from_slice(&val_idx.to_le_bytes());

        // Important flag
        output.push(if decl.important { 0x01 } else { 0x00 });

        Ok(())
    }
}

fn at_rule_contains_keyframes(at_rule: &AtRule) -> bool {
    match at_rule {
        AtRule::Keyframes { .. } => true,
        AtRule::Nested { rules } => rules.iter().any(at_rule_contains_keyframes),
        _ => false,
    }
}

fn rule_type_for_at_rule(at_rule: &AtRule) -> RuleType {
    match at_rule {
        AtRule::Media { .. } => RuleType::Media,
        AtRule::Container { .. } => RuleType::Container,
        AtRule::Keyframes { .. } => RuleType::Keyframes,
        AtRule::Supports { .. } => RuleType::Supports,
        AtRule::Layer { .. } => RuleType::Layer,
        AtRule::Nested { .. } => RuleType::Nested,
        AtRule::Import { .. } => RuleType::Normal,
    }
}

/// Classify a selector for optimization hints.
fn classify_selector(selector: &str) -> SelectorType {
    let trimmed = selector.trim();

    if trimmed == "*" {
        SelectorType::Universal
    } else if trimmed.starts_with('#') && !trimmed.contains(' ') {
        SelectorType::Id
    } else if trimmed.starts_with('.') && !trimmed.contains(' ') {
        SelectorType::Class
    } else if !trimmed.contains(' ')
        && !trimmed.contains('.')
        && !trimmed.contains('#')
        && !trimmed.contains('[')
    {
        SelectorType::Tag
    } else {
        SelectorType::Compound
    }
}

