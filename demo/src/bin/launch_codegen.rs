use std::{error::Error, fs, path::Path};

use style::{core::AppState, parser::extract_classes_fast};

const SOURCE: &str = "demo/site/LaunchPage.dx.jsx";
const HTML_OUT: &str = "demo/site/index.html";
const CSS_OUT: &str = "demo/site/site.css";
const GENERATED_HEADER: &str = "/* generated by dx-style from demo/site/LaunchPage.dx.jsx */";

fn main() -> Result<(), Box<dyn Error>> {
    let source = fs::read_to_string(SOURCE)?;
    let html = render_launch_html(&source)?;
    let css = generate_css(&source)?;

    fs::write(Path::new(HTML_OUT), html)?;
    fs::write(Path::new(CSS_OUT), css)?;

    println!("generated {HTML_OUT}");
    println!("generated {CSS_OUT}");

    Ok(())
}

fn render_launch_html(source: &str) -> Result<String, Box<dyn Error>> {
    let body = extract_return_markup(source)?;
    let html = body
        .replace("className=", "class=")
        .replace("charSet=", "charset=")
        .replace("htmlFor=", "for=")
        .replace("tabIndex=", "tabindex=");

    Ok(format!(
        "<!doctype html>\n{}\n",
        normalize_jsx_void_tags(&html)
    ))
}

fn extract_return_markup(source: &str) -> Result<&str, Box<dyn Error>> {
    let return_start = source
        .find("return (")
        .ok_or("LaunchPage source must contain `return (`")?;
    let markup_start = return_start + "return (".len();
    let return_end = source[markup_start..]
        .rfind("\n  );")
        .ok_or("LaunchPage source must close with `);`")?
        + markup_start;

    Ok(source[markup_start..return_end].trim())
}

fn normalize_jsx_void_tags(markup: &str) -> String {
    markup
        .replace(" />", ">")
        .replace("<meta charset=\"utf-8\">", "<meta charset=\"utf-8\" />")
        .replace(
            "<meta name=\"viewport\" content=\"width=device-width, initial-scale=1\">",
            "<meta name=\"viewport\" content=\"width=device-width, initial-scale=1\" />",
        )
}

fn generate_css(source: &str) -> Result<String, Box<dyn Error>> {
    let extracted = extract_classes_fast(source.as_bytes(), 256);
    let mut classes: Vec<String> = extracted.classes.into_iter().collect();
    classes.sort();

    let engine = AppState::engine();
    let mut unknown = Vec::new();
    let mut rules = Vec::new();

    for class_name in &classes {
        if is_inert_class(class_name) {
            continue;
        }

        if let Some(rule) = engine.css_for_class(class_name) {
            rules.push(minify_rule(&rule));
        } else {
            unknown.push(class_name.clone());
        }
    }

    if !unknown.is_empty() {
        return Err(format!(
            "dx-style could not generate {} classes: {}",
            unknown.len(),
            unknown.join(", ")
        )
        .into());
    }

    rules.sort();
    rules.dedup();

    Ok(format!("{GENERATED_HEADER}\n{}\n", rules.join("")))
}

fn is_inert_class(class_name: &str) -> bool {
    matches!(class_name, "dark")
}

fn minify_rule(rule: &str) -> String {
    let mut out = String::with_capacity(rule.len());
    let mut previous_was_space = false;

    for ch in rule.chars() {
        if ch.is_whitespace() {
            previous_was_space = true;
            continue;
        }

        if previous_was_space && !matches!(ch, '{' | '}' | ':' | ';' | ',' | ')') {
            let last = out.chars().last();
            if !matches!(
                last,
                None | Some('{') | Some('}') | Some(':') | Some(';') | Some(',') | Some('(')
            ) {
                out.push(' ');
            }
        }

        previous_was_space = false;
        out.push(ch);
    }

    out
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn extracts_react_like_markup() {
        let source = r#"export function LaunchPage() {
  return (
    <html className="dark"><body className="m-0 text-zinc-50"></body></html>
  );
}"#;

        let html = render_launch_html(source).expect("html");

        assert!(html.starts_with("<!doctype html>"));
        assert!(html.contains("class=\"m-0 text-zinc-50\""));
    }

    #[test]
    fn fails_on_unknown_style_classes() {
        let error = generate_css(r#"<div className="not-a-real-utility"></div>"#)
            .expect_err("unknown utility should fail");

        assert!(error.to_string().contains("not-a-real-utility"));
    }
}
